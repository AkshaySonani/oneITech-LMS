                        <h2 class="section-heading">Description</h2>
                        <div class="editor-content">
                            <p>KnowledgeHut’s course on CMMI<sup>®</sup> for Development Version 1.3(CMMI<sup>®</sup>-DEV) exposes participants to the best practices that are adopted while implementing CMMI<sup>®</sup> (Capability Maturity Model Integration) models for improving performance and achieving business goals. CMMI<sup>®</sup>-DEV Version 1.3 is the preferred choice of organisations across the world to improve development processes in high-quality engineering. While understanding the fundamental concepts, you will also get a chance to understand how they are applied to practical scenarios through our interactive, hands-on sessions.</p>
                            <p>On successful completion of the course, you will receive a Course Completion Certificate from KnowledgeHut.</p>
                            <h6>What you will learn:</h6>
                            <ul>
                                <li>Understand the need for CMMI framework and its fundamentals</li>
                                <li>Understand the various components of the CMMI-DEV model, the process areas and their relationships</li>
                                <li>Describe the model content</li>
                                <li>Understand the detailed requirements of the process areas in the CMMI<sup>®</sup> model</li>
                                <li>Analyze and make valid judgments regarding the organization's implementation of process areas</li>
                                <li>Learn how to use CMMI and turn losses into opportunities</li>
                                <li>Be able to identify issues that should be addressed in performing process improvements using CMMI<sup>®</sup>-DEV, V1.3</li>
                            </ul>
