                        <div class="cert-and-exam-section" id="pd-hours-pdus-and-cdus-3">
                            <h3>PD hours, PDUs and CDUs</h3>
                            <div class="course-cert-faq-content accordion-cards custom-cards" id="faq-accordion-3">
                                <div class="card">
                                    <div class="card-header"> <a class="card-link collapsed" data-toggle="collapse" href="#faq-heading-3-1">1. What are PD hours, CDUs, and PDUs?</a> </div>
                                    <div id="faq-heading-3-1" class="collapse " data-parent="#faq-accordion-3">
                                        <div class="card-body editor-content">
                                            <p><b>PD hours</b> are Professional Development hours issued by International Institute of Business Analysis (IIBA). They are earned by completing educational training or learning opportunities, which serve to validate your participation and continued proficiency in the principles and practices of Business Analysis in line with the BABOK Guide. 1 hour of Professional Development = 1 Contact Hour.&nbsp;&nbsp;</p>
                                        </div>
                                    </div>
                                </div>
                                <div class="card">
                                    <div class="card-header"> <a class="card-link  collapsed" data-toggle="collapse" href="#faq-heading-3-2">2. What are CDUs?</a> </div>
                                    <div id="faq-heading-3-2" class="collapse " data-parent="#faq-accordion-3">
                                        <div class="card-body editor-content">
                                            <p><b>CDUs</b> are Continuing Development Units needed for maintaining one’s IIBA certification. They can be earned by participating in formal training opportunities, like certified courses from any accreditation body, and other forms of learning opportunities like webinars, self-study courses, etc.&nbsp;<br></p>
                                        </div>
                                    </div>
                                </div>
                                <div class="card">
                                    <div class="card-header"> <a class="card-link  collapsed" data-toggle="collapse" href="#faq-heading-3-3">3. What are PDUs?</a> </div>
                                    <div id="faq-heading-3-3" class="collapse " data-parent="#faq-accordion-3">
                                        <div class="card-body editor-content">
                                            <p><b>PDUs</b> are Professional Development Units issued by the Project Management Institute, Inc (PMI). They can be earned by completing educational training or learning opportunities, which serve to validate your participation and continued proficiency in the principles and practices of project management. &nbsp;</p>
                                            <p>‘PDU’ refers to measuring units used to quantify your professional development as part of the PMI’s Continuing Certification Requirements (CCR) program. 1 PDU = 1 hour of learning/activity. &nbsp;</p>
                                        </div>
                                    </div>
                                </div>
                                <div class="card">
                                    <div class="card-header"> <a class="card-link  collapsed" data-toggle="collapse" href="#faq-heading-3-4">4. Where are PDUs applicable?</a> </div>
                                    <div id="faq-heading-3-4" class="collapse " data-parent="#faq-accordion-3">
                                        <div class="card-body editor-content">
                                            <p>PDUs recognize your dedication to the project management world through the award of credits for every educational effort you make throughout your career. PDUs are offered by the PMI and requires you to earn a specific number of PDUs per three-year certification cycle. &nbsp;<br></p>
                                        </div>
                                    </div>
