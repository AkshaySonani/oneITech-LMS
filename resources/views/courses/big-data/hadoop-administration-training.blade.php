                    <h2 class="section-heading">Hadoop Administration FAQs</h2>
                    <div class="cert-and-exam-section" id="certification-process-hadoop-administration">
                        <h3>Hadoop Administration</h3>
                        <div class="panel-group accordion seo-content-accordion-1" role="tablist" aria-multiselectable="true">
                            <div class="panel panel-default">
                                <div class="panel-heading active" role="tab" id="seo-content-heading-1-1">
                                    <h4 class="panel-title"> <a role="button" data-toggle="collapse" data-parent=".seo-content-accordion-1" href="#seo-content-1-1" aria-expanded="true" aria-controls="seo-content-1-1">What exactly does a Hadoop Administrator do?</a> </h4>
                                </div>
                                <div id="seo-content-1-1" class="panel-collapse collapse in" role="tabpanel" aria-labelledby="seo-content-heading-1-1">
                                    <div class="panel-body">
                                        <div class="editor-content">
                                            <div>
                                                <div>
                                                    <p>A Hadoop administrator administers and manages the set of Hadoop clusters. A Hadoop administrator’s responsibilities include setting up Hadoop clusters, backup, recovery and maintenance of the clusters. Good knowledge of Hadoop architecture is required to become a Hadoop administrator. Some of the key responsibilities of a Hadoop Administrator are:&nbsp;</p>
                                                </div>
                                                <div>
                                                    <ul>
                                                        <li>Takes care of the day-to-day running of Hadoop Clusters.&nbsp;</li>
                                                        <li>Makes sure that Hadoop cluster is running all the time.&nbsp;</li>
                                                        <li>Responsible for managing and reviewing Hadoop log files.&nbsp;</li>
                                                        <li>Responsible for capacity planning and estimating the requirements.&nbsp;</li>
                                                        <li>Implementation of ongoing Hadoop infrastructure.&nbsp;</li>
                                                        <li>Cluster maintenance along with the creation and removal of nodes.&nbsp;</li>
                                                        <li>Keeping an eye on Hadoop cluster security and connectivity.&nbsp;</li>
                                                        <li>Tuning the performance of Hadoop clusters.&nbsp;</li>
                                                        <li>Managing and reviewing the log files of Hadoop.&nbsp;</li>
                                                    </ul>
                                                </div>
                                            </div>
                                        </div>
                                    </div>
                                </div>
                            </div>
                            <div class="panel panel-default">
                                <div class="panel-heading" role="tab" id="seo-content-heading-1-2">
                                    <h4 class="panel-title"> <a class="collapsed" role="button" data-toggle="collapse" data-parent=".seo-content-accordion-1" href="#seo-content-1-2" aria-expanded="false" aria-controls="seo-content-1-2">How does Hadoop work?</a> </h4>
                                </div>
                                <div id="seo-content-1-2" class="panel-collapse collapse" role="tabpanel" aria-labelledby="seo-content-heading-1-2">
                                    <div class="panel-body">
                                        <div class="editor-content">
                                            <p>Hadoop mainly consists of three layers:&nbsp;&nbsp;</p>
                                            <p> </p>
                                            <ul>
                                                <li>HDFS (Hadoop Distributed File System): the place where all the data is stored,&nbsp;&nbsp;</li>
                                                <li>Application layer (on which the MapReduce engine sits): to process the stored data, and&nbsp;</li>
                                                <li>YARN: which allocates the resources to various slaves. All of these are operating on Master and Slave nodes.&nbsp;</li>
                                            </ul>
                                        </div>
                                    </div>
                                </div>
                            </div>
                            <div class="panel panel-default">
                                <div class="panel-heading" role="tab" id="seo-content-heading-1-3">
                                    <h4 class="panel-title"> <a class="collapsed" role="button" data-toggle="collapse" data-parent=".seo-content-accordion-1" href="#seo-content-1-3" aria-expanded="false" aria-controls="seo-content-1-3">Is Hadoop a framework or a programming language?</a> </h4>
                                </div>
                                <div id="seo-content-1-3" class="panel-collapse collapse" role="tabpanel" aria-labelledby="seo-content-heading-1-3">
                                    <div class="panel-body">
                                        <div class="editor-content">
                                            <p>Hadoop is an open-source framework written in Java that enables the distributed processing of large datasets. Hadoop is not a programming language.&nbsp;</p>
                                        </div>
                                    </div>
                                </div>
                            </div>
