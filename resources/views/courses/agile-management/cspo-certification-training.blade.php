                                    <div class="panel-body">
                                        <div class="editor-content">
                                            <p>A Product Owner is a person who determines what a Scrum team delivers to stakeholders. Product Owners are a liaison between the development team and stakeholders such as end-users, marketing, sales, customer support, higher management, etc. They carry the vision and manage the user feedback. All Scrum Team needs a Product Owner, as without them, the team will not deliver what is most valuable to stakeholders. As a Product Owner, you must have the following skills:</p>
                                            <ul>
                                                <li><b>Communication skills</b> - A Product Owner works with many stakeholders to understand their vision and helps the development team to bring it to reality. Without good communication skills there will be less alignment amongst stakeholders and less fruitful interactions between stakeholders and the team.</li>
                                                <li><b>Commitment</b> - As a Product Owner, you should be committed to the vision, business goals and the team.</li>
                                                <li><b>Power of No</b> - A Product Owner is responsible for managing the requests and feedback from stakeholders. Many times, they have to say ‘No’. Developers cannot develop every idea or feedback the stakeholders have, because there are always limits on time and budget. That is why it is important to say ‘No’ in a manner that does not upset or discourage the business.</li>
                                                <li><b>Vision</b> - Product Owners are responsible for seeing and communicating the product’s full vision and steering the ship forward. They also align the vision of the product with the vision and needs of the company. Furthermore, a Product Owner needs to communicate the relation between the business goals and the backlog items.</li>
                                                <li><b>Curiosity</b> - As a Product Owner, you must be curious to ask ‘Why’ from business and ‘how’ from the development team. And bring stakeholders together with the development team to stimulate unexpected solutions to occur.</li>
                                            </ul>
                                            <p>All the above-mentioned skills are linked together with communication. A Product Owner must be a master communicator and ensure that stakeholders and Scrum Team are aligned together for making the vision come true.</p>
