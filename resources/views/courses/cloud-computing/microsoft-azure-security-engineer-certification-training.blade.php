                        <div class="cert-and-exam-section" id="azure-security-engineer-certification-1">
                            <h3>Azure Security Engineer Certification</h3>
                            <div class="course-cert-faq-content accordion-cards custom-cards" id="faq-accordion-1">
                                <div class="card">
                                    <div class="card-header"> <a class="card-link collapsed" data-toggle="collapse" href="#faq-heading-1-1">1. What can I expect to accomplish after completing the Microsoft Azure Security Engineer Course?</a> </div>
                                    <div id="faq-heading-1-1" class="collapse " data-parent="#faq-accordion-1">
                                        <div class="card-body editor-content">
                                            <p>Our Microsoft Azure Security Engineer certification training has been designed to help you gain expertise implementing Azure security controls that protect identity, access, data, applications, and networks in cloud and hybrid environments. You’ll gain the confidence to take other courses in Microsoft Azure. By the end of the course, you will gain the expertise to:&nbsp;</p>
                                            <ul>
                                                <li>Leverage the benefits of Azure for your business’ cloud computing needs&nbsp;</li>
                                                <li>Understand the various services available including computing, networking, storage, and databases&nbsp;</li>
                                                <li>Choose the best security tools and services for your needs&nbsp;</li>
                                            </ul>
                                        </div>
                                    </div>
                                </div>
                                <div class="card">
                                    <div class="card-header"> <a class="card-link  collapsed" data-toggle="collapse" href="#faq-heading-1-2">2. What practical skills can I expect to have after completing the Microsoft Azure Security Engineer Course?</a> </div>
                                    <div id="faq-heading-1-2" class="collapse " data-parent="#faq-accordion-1">
                                        <div class="card-body editor-content">
                                            <p>Our Azure Security Engineer Training aims to equip you with all the skills to be an effective Azure practitioner. You will develop practical skills across the breadth of Azure services:&nbsp;</p>
                                            <ul>
                                                <li>Implement enterprise governance strategies including role-based access control, Azure policies, and resource locks.&nbsp;</li>
                                                <li>Implement an Azure AD infrastructure including users, groups, and multi-factor authentication.&nbsp;</li>
                                                <li>Implement Azure AD Identity Protection including risk policies, conditional access, and access reviews.&nbsp;</li>
                                                <li>Implement Azure AD Privileged Identity Management including Azure AD roles and Azure resources.&nbsp;</li>
                                                <li>Implement Azure AD Connect including authentication methods and on-premises directory synchronization.&nbsp;</li>
                                                <li>Implement perimeter security strategies including Azure Firewall.&nbsp;</li>
                                                <li>Implement network security strategies including Network Security Groups and Application Security Groups.&nbsp;</li>
                                                <li>Implement host security strategies including endpoint protection, remote access management, update management, and disk encryption.&nbsp;</li>
                                                <li>Implement container security strategies including Azure Container Instances, Azure Container Registry, and Azure Kubernetes.&nbsp;</li>
