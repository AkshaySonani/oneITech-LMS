                        <h2 class="section-heading">Description</h2> <span class="reg-provider"> <i class="fa fa-check-square"></i> <span>Regd. Education Provider</span> <i class="accrediation-icons acdtn-page acdtn-page-10 lazyloaded" style="background-image: url(&quot;https://d2o2utebsixu4k.cloudfront.net/assets/icons/acd-sprite.png&quot;);"></i> </span>
                        <div class="editor-content">
                            <p>Every organization, whether it is a commercial enterprise, government agency, or a not-for profit organization, must have established guidelines that will protect it from business risks. The ISO/IEC 27000 suite of standards define exactly these requirements and form a formal specification that help organizations establish, implement, operate, monitor, review, maintain and improve a documented Information Security Management System. As an ISO 27000 certified professional, you can help an organization demonstrate achievement of excellence and compliance with global best practices for quality in Information Security Management.</p>
                            <p>KnowledgeHut helps you prepare for the ISO 27000 Foundation certification provided by Peoplecert where candidates will be introduced to the principles and core elements of the ISO 27000, specifically for ISO/IEC 27001 and ISO/IEC 27000. With comprehensive courseware, in-depth exercises, and training from experienced professionals, participants can aim for a first time clearance of the examination and apply the ISO 27000 standard to ensure continuity and effectiveness of the organization.</p>
                            <h6><b>What you will learn:</b><br></h6>
                            <p>As this is the Foundation level course, candidates will be introduced to the principles and core elements of the ISO/IEC 27001 and ISO/IEC 27002 standards for Information Security Management, and more specifically:</p>
                            <ul>
                                <li>ISO/IEC 27000: which provides an overview of information security management systems, which form the subject of the ISMS family of standards, and defines related terms. </li>
                                <li>ISO/IEC 27001: the formal specification which defines the requirements that must be achieved for an information security management system (ISMS). </li>
                                <li>ISO/IEC 27002: which describes a code of practice for information security management and details hundreds of specific controls which may be applied to secure information and related assets</li>
                            </ul>
                            <p>Holders of Peoplecert's ISO 27000: Information Security Management Foundation Certification will be able to demonstrate their knowledge, ability, competence and understanding in:</p>
                            <ul>
                                <li>Definitions and principles of quality management services in accordance with ISO/IEC 27001. </li>
                                <li>Positioning of ISO/IEC 20000 in the Information security management including its relationship with other standards and best practices. </li>
                                <li>Objectives and requirements in each section of the specification. </li>
                                <li>Scope, aims and use of the ISO/IEC 27001 and ISO/IEC 27002 Specification and Code of Practice. </li>
                                <li>Processes and objectives of ISO/IEC 27001 and ISO/IEC 27002 and Information security management (ISMS). </li>
                                <li>Fundamental requirements for an Information Security Management System (ISMS). </li>
                                <li>Requirements of the Information Security Management System and the Plan, Do, Check, Act cycle. </li>
                                <li class="nitro-offscreen">How assessments, reviews and internal audits of Information Security Management systems against the requirements of the standard are used.</li>
                            </ul>
